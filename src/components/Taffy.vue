  <script>
  export default {
    name: 'CharacterEvolution',
    data() {
      return {
        scrollY: 0
      };
    },
    mounted() {
      // Анимация элементов при скролле
      this.animateOnScroll();
      window.addEventListener('scroll', this.handleScroll);
      
      // Инициализируем тултипы на изображениях
      this.initializeImageHovers();
      
      // Добавляем класс для анимированного появления
      setTimeout(() => {
        document.querySelector('.character-evolution').classList.add('loaded');
      }, 100);
    },
    beforeDestroy() {
      window.removeEventListener('scroll', this.handleScroll);
    },
    methods: {
      handleScroll() {
        this.scrollY = window.scrollY;
        this.animateOnScroll();
        this.parallaxEffect();
      },
      animateOnScroll() {
        const elements = document.querySelectorAll('.timeline-item, .philosophy-item, .thanks-card');
        
        elements.forEach(element => {
          const elementTop = element.getBoundingClientRect().top;
          const elementBottom = element.getBoundingClientRect().bottom;
          const windowHeight = window.innerHeight;
          
          if (elementTop < windowHeight * 0.85 && elementBottom > 0) {
            element.classList.add('animate');
          }
        });
      },
      parallaxEffect() {
        const parallaxHeader = document.querySelector('.parallax-header');
        if (parallaxHeader) {
          const offset = window.scrollY;
          parallaxHeader.style.backgroundPositionY = `${50 + (offset * 0.1)}%`;
        }
      },
      initializeImageHovers() {
        const images = document.querySelectorAll('.character-card img');
        
        images.forEach(img => {
          img.addEventListener('mouseenter', function() {
            this.style.transition = 'transform 8s ease';
            this.style.transform = 'scale(1.25) rotate(2deg)';
          });
          
          img.addEventListener('mouseleave', function() {
            this.style.transition = 'transform 0.5s ease';
            this.style.transform = 'scale(1) rotate(0deg)';
          });
        });
      }
    }
  }
  </script>
  
